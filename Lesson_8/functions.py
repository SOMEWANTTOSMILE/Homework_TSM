def summ(a, b):
    total = a + b
    return total


def subtraction(a, b):
    total = a - b
    return total


def multiplication(a, b):
    total = a * b
    return total


def division(a, b):
    total = a / b
    return total


def exponentiation(a, b):
    total = a ** b
    return total

